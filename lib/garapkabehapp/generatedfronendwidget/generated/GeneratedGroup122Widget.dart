                    (constraints.maxWidth * percentWidth) / 6.51300048828125;

                double percentHeight = 0.04995932553052827;
                double scaleY = (constraints.maxHeight * percentHeight) /
                    10.599977493286133;

                return Stack(children: [
                  TransformHelper.translateAndScale(
                      translateX: constraints.maxWidth * 0.4974128920198163,
                      translateY: constraints.maxHeight * 0.9288032274057934,
                      translateZ: 0,
                      scaleX: scaleX,
                      scaleY: scaleY,
                      scaleZ: 1,
                      child: GeneratedPath693Widget())
                ]);
              }),
            )
